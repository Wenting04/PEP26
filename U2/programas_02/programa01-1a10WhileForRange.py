# Mostrar lista de números del 1 al 10 usando bucles for y while
# Cuando se use for, se puede usar la función range #

print("Nº del 1 al 10 con while: ")
cont = 1
while cont <= 10:
    print(cont)
    cont += 1

print("Nº del 1 al 10 usando for con range: ")
for i in range(1, 11): #Imprime de 1 al 10
    print(i)