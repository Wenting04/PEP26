# Usando print() hay que hacer que imprima:
# El nº 34 en binario
#   otro en octal
#   otro en hexadecimal
# Los nº 0b1001, 0o12, 0xa3f en decimal.#

x = 34
print(x," en binario es:", bin(x))
print(x," en octal es:", oct(x))
print(x," en hexadecimal es:", hex(x))

print()

# Dentro del propio nº tiene los prefijos para usarse en conversión 
#   Funciones incluye prefijo
#       bin() -> 0b
#       oct() -> 0o
#       hex() -> 0x
# #
print("0b1001 en decimal es:", 0b1001)
print("0o12 en decimal es:", 0o12)
print("0xa3f en decimal es:", 0xa3f)
