# Preguntar al usuario 2 nº y calcular su suma, resta, multiplicación, división, módulo y potencia#

num1 = float(input("1º Número: "))
num2 = float(input("2º Número: "))

print("Suma: ", num1, " + ", num2, " = ", num1 + num2)
print("Resta: ", num1, " - ", num2, " = ", num1 - num2)
print("Multiplicación: ", num1, " * ", num2, " = ", num1 * num2)
print("División: ", num1, " / ", num2, " = ", num1 / num2)
print("Módulo: ", num1, " % ", num2, " = ", num1 % num2)
print("Potencia: ", num1, " ** ", num2, " = ", num1 ** num2)