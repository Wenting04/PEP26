# Usando 1 de los operadores de comparación
# Programa simple de 2 líneas
# Tomar por parámetro n como entrada (un entero)
#  Y debe imprimir False si
#   n menor que 100
# Y True si
#   n mayor o igual que 100 
# Ningún if#

n = int(input("Introduce nº entero: "))
print(n >= 100)

# Para que False si
#   n < 100
# Y True si
#   n >= 100
# Podemos directamente poner si es True o False
#   cuando se cumple la verdadera: n >= 100
#   Pq si no es cierto, imprime False#